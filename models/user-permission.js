// ----------------------------------------------------------------------------------------------------------
// dependencies
// ----------------------------------------------------------------------------------------------------------

var mongoose = require("mongoose");

// ----------------------------------------------------------------------------------------------------------
// main script
// ----------------------------------------------------------------------------------------------------------

var UserPermission = new mongoose.Schema({

    userId:
    {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'users'
    },

    entitlement: String,
    target:      mongoose.Schema.Types.ObjectId

});

UserPermission.index({userId: 1});

module.exports = mongoose.model('user_permissions', UserPermission);

