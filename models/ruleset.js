// ----------------------------------------------------------------------------------------------------------
// dependencies
// ----------------------------------------------------------------------------------------------------------

var mongoose = require("mongoose");

// ----------------------------------------------------------------------------------------------------------
// main script
// ----------------------------------------------------------------------------------------------------------

var Ruleset = new mongoose.Schema(
{
    /**
     * display name for the game
     */
    name: String,

    /**
     * path-friendly name for the game
     */
    path: String,

    /**
     * Description of the game
     */
    description: String,

    /**
     * Icon for the game
     */
    icon: String,

    /**
     * Allotted file space for the game
     */
    allotedSpace: Number
});

module.exports = mongoose.model('rulesets', Ruleset);

